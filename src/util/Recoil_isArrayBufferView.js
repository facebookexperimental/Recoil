'use strict';

const arrayBufferViewTypes = [
  Int8Array,
  Uint8Array,
  Uint8ClampedArray,
  Int16Array,
  Uint16Array,
  Int32Array,
  Uint32Array,
  Float32Array,
  Float64Array,
  DataView,
];
function isArrayBufferView(value: mixed): boolean {
  return arrayBufferViewTypes.some(type => value instanceof type);
}

module.exports = isArrayBufferView;
